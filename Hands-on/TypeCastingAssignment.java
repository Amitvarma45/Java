package Corejava;

public class TypeCastingAssignment {
	public static void main(String[] args) {
		
		
		//Implicit int to double
		int i = 36;
		
		double d = i;
		
		System.out.println("Int value is "+i);
		System.out.println("Double value is "+d);
		
		//Explicit double to int
		double db = 39.78;
		
		int it = (int)db;
		
		System.out.println("Double value is "+db);
		System.out.println("Integer value is "+it);
		
		
	}

}
