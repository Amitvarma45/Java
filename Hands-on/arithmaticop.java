package Corejava;

public class arithmaticop {
	public static void main(String[] args)
	{
		int a= 10;
		int b= 20;
		int c;
		c=a+b;
		System.out.println(a+"+"+b+"="+c);
		c=a-b;
		System.out.println(a+"-"+b+"="+c);
		c=a*b;
		System.out.println(a+"*"+b+"="+c);
		c=a/b;
		System.out.println(a+"/"+b+"="+c);
		c=a%b;
		System.out.println(a+"%"+b+"="+c);
		
	}
	

}
