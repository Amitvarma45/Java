package Corejava;

public class Boxing {

	public static void main(String[] args) {
		// Assigning primitive long value to wrapper long variable x
		Long x=430L;
		// Assigning primitive char value to Wrapper Character variable y.
		Character y='a';
		int a = 50;
		Integer a2 = new Integer(a);
		Integer a3 = 5;//Boxing
		System.out.println(a2+""+a3);
		System.out.println(x);
		System.out.println(y);
		

	}

}
