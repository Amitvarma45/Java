package Corejava;

public class wideningcasting {
	public static void main(String[] args)
	{
		int i=200;
		
		//automatic type conversion
		long l=i;
		
		//automatic type conversion
		float f=l;
		
		System.out.println("Int value"+i);
		System.out.println("Long Value"+l);
		System.out.println("Float Value"+f);
	}

}
